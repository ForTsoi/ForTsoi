document.addEventListener("DOMContentLoaded", () => {
  const authButtons = document.getElementById("authButtons");
  const profileContainer = document.getElementById("profileContainer");
  const profilePic = document.getElementById("profilePic");
  const profileName = document.getElementById("profileName");
  const logoutButton = document.getElementById("logoutButton");

  // Перевірка, чи є користувач у LocalStorage
  const currentUser = JSON.parse(localStorage.getItem("currentUser"));

  if (currentUser) {
    // Приховуємо кнопки "Увійти" та "Зареєструватися"
    authButtons.style.display = "none";

    // Відображаємо блок профілю
    profileContainer.classList.remove("hidden");
    profilePic.src = currentUser.profilePic;
    profileName.textContent = currentUser.username;
  }

  // Обробка кнопки "Вийти"
  logoutButton.addEventListener("click", () => {
    // Видаляємо дані користувача з LocalStorage
    localStorage.removeItem("currentUser");

    // Приховуємо блок профілю
    profileContainer.classList.add("hidden");

    // Повертаємо кнопки "Увійти" та "Зареєструватися"
    authButtons.style.display = "flex";
  });
});

